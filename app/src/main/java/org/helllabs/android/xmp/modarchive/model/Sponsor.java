package org.helllabs.android.xmp.modarchive.model;

public class Sponsor {
	private String name;
	private String link;
	
	public String getName() {
		return name;
	}
	
	public void setName(final String name) {
		this.name = name;
	}
	
	public String getLink() {
		return link;
	}
	
	public void setLink(final String link) {
		this.link = link;
	}
}
